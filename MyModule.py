class MyMaths:

    def __init__(self, op1, op2):
        self.op1 = op1
        self.op2 = op2

    def addition(self):
        res = self.op1 + self.op2
        return res

    def subtraction(self):
        res = self.op1 - self.op2
        return res

    def multiplication(self):
        res = self.op1 * self.op2
        return res

    def division(self):
        res = self.op1 / self.op2
        return res
    